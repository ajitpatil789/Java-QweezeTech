package com.ajit.java.VariablesAndDatatypes;

// Variables and Data types.

public class VariablesAndDatatypes {
    public int ten = 10;

    public static void main(String[] args) {
        // Declare and initialize variables of different data types
        byte byteValue = 12; //Smallest integer (-128 to 127)
        short shortValue = 123;
        int intValue = 100;
        float floatValue = 10.5f;
        double doubleValue = 10.52;
        long longValue = 537810106698L;
        char charValue = 'a';
        boolean isAdult = true;
        String stringValue = "Welcome to Java World";

        // Print the values of the variables
        System.out.println("Byte: " + byteValue);
        System.out.println("Short: " + shortValue);
        System.out.println("Int: " + intValue);
        System.out.println("Float: " + floatValue);
        System.out.println("Double: " + doubleValue);
        System.out.println("Long: " + longValue);
        System.out.println("Char: " + charValue);
        System.out.println("String: " + stringValue);
        System.out.println("Boolean: " + isAdult);

    }
}